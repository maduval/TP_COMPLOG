<?php

namespace imie\ComplogBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use Doctrine\Common\Collections\ArrayCollection;

/**
 * Product
 *
 * @ORM\Table()
 * @ORM\Entity(repositoryClass="imie\ComplogBundle\Repository\ProductRepository")
 */
class Product
{
    //--------------
    // ENTITY FIELDS
    //--------------

    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;

    /**
     * @var string
     *
     * @ORM\Column(name="description", type="string", length=255)
     */
    private $description;

    /**
     * @var float
     *
     * @ORM\Column(name="price", type="float")
     */
    private $price;

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Product
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set description
     *
     * @param string $description
     *
     * @return Product
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * Set price
     *
     * @param float $price
     *
     * @return Product
     */
    public function setPrice($price)
    {
        $this->price = $price;

        return $this;
    }

    /**
     * Get price
     *
     * @return float
     */
    public function getPrice()
    {
        return $this->price;
    }

    //------------------------------
    // RELATIONSHIP BETWEEN ENTITIES
    //------------------------------

    /**
     * @ORM\OneToMany(targetEntity="CustOrderDetail", mappedBy="product")
     */
    private $custOrderDetails;

    /**
     * Product constructor.
     */
    public function __construct()
    {
        $this->custOrderDetails = new ArrayCollection();
    }

    /**
     * @return mixed
     */
    public function getCustOrderDetails ()
    {
        return $this->custOrderDetails;
    }

    /**
     * @param mixed $custOrderDetails
     */
    public function setCustOrderDetails ( $custOrderDetails )
    {
        $this->custOrderDetails = $custOrderDetails;
    }


}

